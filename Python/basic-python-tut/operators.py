# Assigning values to variables
a, b = 3, 4

# Multiplication
print("Multiplication: ", a * b)

# Exponentiation
print("Exponentiation: ", a ** b)

# Bitwise AND
print("Bitwise AND: ", a & b)

# Floor Division
print("Floor Division: ", a // b)

## Compound Operations

a += 5
print("a += 5: ", a)

b *= a
print("b *= a: ", b)

''' Multiplication:  12
Exponentiation:  81
Bitwise AND:  0
Floor Division:  0
a += 5:  8
b *= a:  32 '''
